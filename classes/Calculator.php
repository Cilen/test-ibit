<?php


class Calculator
{
    public static function addition ($firstNumber, $lastNumber){
        return $firstNumber + $lastNumber;
    }
    public static function subtraction ($firstNumber, $lastNumber){
        return $firstNumber - $lastNumber;
    }
    public static function multiplication ($firstNumber, $lastNumber){
        return $firstNumber * $lastNumber;
    }
    public static function division ($firstNumber, $lastNumber){
        return $firstNumber / $lastNumber;
    }

}